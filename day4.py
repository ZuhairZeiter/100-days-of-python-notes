# 1. Getting to know Python Functions
# Functions are blocks of code that can be reused in your program.
# Functions can perform a specific task and can be called multiple times in your program.
# Functions can also take input parameters and return output values.
# Functions are declared using the def keyword.
# Example:
def my_function():
    print("Hello from a function")  

# 2. Python Function Declaration
# Functions are declared using the def keyword.
# Example:
def my_function():
    print("Hello from a function")  

# 3. Types of Functions
# There are two types of functions in Python:
# 1. Built-in functions
# 2. User-defined functions
# Built-in functions are functions that are built into Python.
# User-defined functions are functions that are defined by the user.
# Example:

def my_function():
    print("Hello from a function")

my_function()

# 4. Creating and calling a Function with Parameters
# Functions can take input parameters and return output values.
# Example:
def add_numbers(a, b):
    return a + b

result = add_numbers(2, 3)
print(result)

